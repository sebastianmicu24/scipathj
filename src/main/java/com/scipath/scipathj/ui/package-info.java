/**
 * User Interface package for SciPathJ application.
 *
 * <p>This package contains the user interface components organized around
 * the three main functionalities of the SciPathJ scientific image analysis application:</p>
 *
 * <h3>Package Structure</h3>
 * <ul>
 *   <li><strong>analysis/</strong>: Components for performing image analysis operations</li>
 *   <li><strong>analysis/components/</strong>: Analysis-specific UI components and panels</li>
 *   <li><strong>common/</strong>: Shared UI components used across multiple features</li>
 *   <li><strong>common/components/</strong>: Reusable UI components and main application panels</li>
 *   <li><strong>controllers/</strong>: UI controllers handling user interactions and business logic</li>
 *   <li><strong>dataset/</strong>: Tools for creating and managing classification datasets</li>
 *   <li><strong>main/</strong>: Main application window and entry point</li>
 *   <li><strong>model/</strong>: UI data models and configuration objects</li>
 *   <li><strong>themes/</strong>: Theme management and visual styling</li>
 *   <li><strong>utils/</strong>: UI utility classes and helper functions</li>
 *   <li><strong>visualization/</strong>: Components for results visualization (future development)</li>
 * </ul>
 *
 * <h3>Architecture Principles</h3>
 * <ul>
 *   <li><strong>Separation of Concerns</strong>: UI logic separated from business logic</li>
 *   <li><strong>Single Responsibility</strong>: Each component has a focused purpose</li>
 *   <li><strong>Feature-Based Organization</strong>: Code organized by functionality</li>
 *   <li><strong>Reusable Components</strong>: Common elements in {@code common} package</li>
 * </ul>
 *
 * @author Sebastian Micu
 * @version 1.0.0
 * @since 1.0.0
 */
